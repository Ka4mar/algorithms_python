# В этом случае сложжность будет Линейная , вариант линейной сложности будет предпочтительнеедля использования
# из-за скорости выполнения и производительности следовательно эффективность этого варианта больше


company = {1: {"name": "Alpha", "profit": 100000}, 2: {"name": "Hp", "profit": 300000},
           3: {"name": "Avalon", "profit": 540000}, 4: {"name": "NoName", "profit": 220000},
           5: {"name": "Avers", "profit": 642000}, 6: {"name": "JQ", "profit": 980000}}


# print(company[1]["profit"])


def highest_profit(dic):

    one = {"name": "", "profit": 0}
    result = [one, one, one]

    for i in dic:
        if dic[i]["profit"] > result[0]["profit"]:
            result[2] = result[1]
            result[1] = result[0]
            result[0] = dic[i]
        else:
            if dic[i]["profit"] > result[1]["profit"]:
                result[2] = result[1]
                result[1] = dic[i]
            else:
                if dic[i]["profit"] > result[2]["profit"]:
                    result[2] = dic[i]

    return result


print(highest_profit(company))


