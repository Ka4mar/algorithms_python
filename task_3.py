array_company = [["Alpha", 100000], ["Hp", 300000], ["Avalon", 540000], ["NoName", 220000],
                 ["Avers", 642000], ["JQ", 980000]]


def highest_profit_arr(arr):

    result = [["one", 0], ["two", 0], ["tree", 0]]

    for i in arr:
        num = 0
        for e in result:
            if i[1] > e[1]:

                result.insert(num, i)
                result.pop()
                break
            else:
                num += 1

    return result


